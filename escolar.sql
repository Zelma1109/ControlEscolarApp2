create table Carreras(
idCarrera int primary key auto_increment,
nombre varchar(100)
);
------------------------------------------------
create table Materias(
matMateria varchar(10) primary key,
nombre varchar(100),
horas int,
creditos int,
fkCarrera int,
fkPredecesor varchar(10) null,
fkAntecesor varchar(10) null,
foreign key (fkCarrera) REFERENCES Carreras(idCarrera),
foreign key (fkPredecesor) REFERENCES Materias(matMateria),
foreign key (fkAntecesor) REFERENCES Materias(matMateria)
);
------------------------------------------------
create table Grupos(
idGrupo int primary key auto_increment,
semestre varchar(5),
generacion varchar(15),
fkCarrera int,
foreign key (fkCarrera) references Carreras(idCarrera)
);
------------------------------------------------
create table GruposxAlumnos(
idUnion int primary key auto_increment,
fkGrupo int,
fkAlumno varchar(10),
foreign key (fkGrupo) references Grupos(idGrupo),
foreign key (fkAlumno) references Alumnos(noControl)
);
------------------------------------------------
create table GruposxMaterias(
idUnionGM int primary key auto_increment,
fkGrupo int,
fkMaterias varchar(10),
foreign key (fkGrupo) references Grupos(idGrupo),
foreign key (fkMaterias) references Materias(matMateria)
);
------------------------------------------------
