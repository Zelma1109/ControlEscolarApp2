select GruposxMaterias.idUnionGM, Grupos.semestre, materias.nombre from grupos, gruposxmaterias, materias
where gruposxmaterias.fkGrupo = grupos.idGrupo and gruposxmaterias.fkMaterias = materias.matMateria;

select * from vistaUnionGM;

